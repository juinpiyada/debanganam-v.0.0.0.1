  <script>
    // Initialize layout and animations
    document.addEventListener('DOMContentLoaded', function() {
      initHeaderScripts();
      initScrollReveal();
      initFormHandler();
    });

    // Header scripts initialization
    function initHeaderScripts() {
      const mobileMenuBtn = document.getElementById('mobile-menu-btn');
      const mobileMenu = document.getElementById('mobile-menu');
      const header = document.getElementById('main-header');

      if (mobileMenuBtn && mobileMenu) {
        mobileMenuBtn.addEventListener('click', () => {
          mobileMenu.classList.toggle('hidden');
        });

        // Close menu on link click
        mobileMenu.querySelectorAll('a').forEach(link => {
          link.addEventListener('click', () => {
            mobileMenu.classList.add('hidden');
          });
        });
      }

      // Sticky header shadow
      if (header) {
        window.addEventListener('scroll', () => {
          if (window.scrollY > 10) {
            header.classList.add('shadow-lg');
          } else {
            header.classList.remove('shadow-lg');
          }
        });
      }
    }

    // Scroll reveal animation
    function initScrollReveal() {
      const reveals = document.querySelectorAll('.reveal');
      
      const observerOptions = {
        root: null,
        rootMargin: '0px',
        threshold: 0.1
      };

      const observer = new IntersectionObserver((entries) => {
        entries.forEach((entry, index) => {
          if (entry.isIntersecting) {
            // Stagger animation
            setTimeout(() => {
              entry.target.classList.add('active');
            }, index * 100);
            observer.unobserve(entry.target);
          }
        });
      }, observerOptions);

      reveals.forEach(reveal => {
        observer.observe(reveal);
      });
    }

    // Form handler
    function initFormHandler() {
      const form = document.getElementById('admission-form');
      if (form) {
        form.addEventListener('submit', function(e) {
          e.preventDefault();
          
          const name = document.getElementById('name').value;
          const phone = document.getElementById('phone').value;
          const message = document.getElementById('message').value;
          
          // Create WhatsApp message
          const whatsappMessage = `Hello, I am ${name}. I would like to enquire about admission at Debanganam Old Age Home. Phone: ${phone}. ${message ? 'Message: ' + message : ''}`;
          
          // Open WhatsApp
          window.open(`https://wa.me/917605869720?text=${encodeURIComponent(whatsappMessage)}`, '_blank');
          
          // Reset form
          form.reset();
          
          // Show success message
          alert('Thank you for your enquiry! We will contact you shortly.');
        });
      }
    }

    // Smooth scroll for anchor links
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
      anchor.addEventListener('click', function(e) {
        e.preventDefault();
        const target = document.querySelector(this.getAttribute('href'));
        if (target) {
          const headerOffset = 80;
          const elementPosition = target.getBoundingClientRect().top;
          const offsetPosition = elementPosition + window.pageYOffset - headerOffset;

          window.scrollTo({
            top: offsetPosition,
            behavior: 'smooth'
          });
        }
      });
    });
